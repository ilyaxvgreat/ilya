package basics_of_software_code_development.celces.task8;

public class TwoNumbers {
    private int a;
    private int b;

    public TwoNumbers(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }
}

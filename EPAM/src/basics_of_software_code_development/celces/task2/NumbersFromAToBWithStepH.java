package basics_of_software_code_development.celces.task2;

public class NumbersFromAToBWithStepH {
    private int a;
    private int b;
    private int step;

    public NumbersFromAToBWithStepH(int a, int b, int step) {
        this.a = a;
        this.b = b;
        this.step = step;
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }

    public int getStep() {
        return step;
    }
}

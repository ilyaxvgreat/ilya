package basics_of_software_code_development.bifurcation.task1;

public class Triangle {
    private int ang1;
    private int ang2;
    private int ang3;

    public Triangle(int ang1, int ang2, int ang3) {
        this.ang1 = ang1;
        this.ang2 = ang2;
        this.ang3 = ang3;
    }

    public int getAng1() {
        return ang1;
    }

    public int getAng2() {
        return ang2;
    }

    public int getAng3() {
        return ang3;
    }
}

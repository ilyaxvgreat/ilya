package basics_of_software_code_development.bifurcation.task4;

public class Brick {
    private int x;
    private int y;
    private int z;

    public Brick(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getZ() {
        return z;
    }
}

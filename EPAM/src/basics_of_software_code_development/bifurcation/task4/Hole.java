package basics_of_software_code_development.bifurcation.task4;

public class Hole {
    private int a;
    private int b;

    public Hole(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }
}

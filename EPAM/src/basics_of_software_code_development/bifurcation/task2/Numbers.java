package basics_of_software_code_development.bifurcation.task2;

public class Numbers {
    private int a;
    private int b;
    private int c;
    private int d;

    public Numbers(int a, int b, int c, int d) {
        this.a = a;
        this.b = b;
        this.c = c;
        this.d = d;
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }

    public int getC() {
        return c;
    }

    public int getD() {
        return d;
    }
}
